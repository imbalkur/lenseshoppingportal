	<h2>Types of Prescription Lenses</h2>
	<p>Through LensesRx you are able to purchase the perfect prescription lenses necessary for your vision correction. Be it single vision driving glasses, reading glasses, or multi-focal progressive glasses, LensesRx has exactly what you need. </p>
	<h3>Single Vision Lenses</h3>
	<p>Single vision prescription lenses contain the same amount of vision correction over the entire lens. These lenses can correct nearsightedness, farsightedness, and astigmatism. Single vision lenses can come in a couple varieties. In terms of reading glasses, full frame reading glasses are your standard readers. Being wider, they provide a larger line of up-close vision. Single vision lenses can also be half frame reading glasses that are more narrow frames. These sit at the end of the nose, leaving your line of vision open for seeing things in the distance.</p><br />
	<h4>Seeing Clearly in the Distance</h4>
	<p>If you have difficulties focusing on objects in the far distance (shortsighted), single vision lenses will assist you to see clearly and carry out activities such as driving and playing sport with confidence.</p>
	<h4>Seeing Clearly up Close</h4>
	<p>If you have trouble focusing on objects up close (longsighted), with single vision lenses you can continue to undertake close tasks such as reading and sewing with ease. These lenses are commonly known as Readers.<br />
	Extended Readers are another option which provide the additional benefit of an intermediate viewing zone as well as a close viewing zone. If you work in an office environment or spend a bit of time on a computer, this option can deliver greater benefits and flexibility. </p>
	<h3>Bifocals Lenses</h3>
	<p>Bifocal Lenses contain two different prescriptions in the same lens. The primary purpose of glasses bifocals is to provide the optimal balance between distance vision and near vision focusing needs. Eyeglasses bifocal lenses are available with the reading segments in a variety of shapes and widths including sunglasses bifocal options. The most popular reading bifocal is the Flat-Top (also known as straight-top or D-segment), 28 millimeters wide. Eyeglass Lens Direct offers a number of options for the eyeglasses bifocal lens wearer. </p>
	<h3>Progressive Lenses</h3>
	<p>Progressive lenses also known as no line bifocals include multiple levels of vision in a single lens. The advantage of progressive lenses is that unlike bifocal and trifocal lenses which allow only two or three levels of vision, they can support multiple levels of vision with focus range shifting gradually in a gradient like fashion with distance vision on the top progressing down to intermediate and near sight vision correction at the bottom. It incorporates multiple vision fields smoothly resulting with vision correction representing closest to natural vision.<br />
	The only downside is that it takes a little while to get used to progressive lenses. Changes in vision occur towards the lenses edges result in altering the peripheral vision slightly. For some people the change is a little difficult. </p><br /><br /><br /><br />
